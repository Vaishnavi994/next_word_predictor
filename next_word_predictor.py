import tensorflow as tf
from tensorflow.keras.preprocessing.text import Tokenizer
#tokenizer converts text -> numbers

tokenizer=Tokenizer()

print(type(faqs))

tokenizer.fit_on_texts([faqs])
#list of strings and sorts by frequncy

tokenizer.word_index

for sentence in faqs.split('\n'):
  print(sentence)

for senetence in faqs.split('\n'):
  print(tokenizer.texts_to_sequences([senetence])[0])

input_sequ=[]
for sentence in faqs.split('\n'):
  tokenized_sentence=tokenizer.texts_to_sequences([sentence])[0]
  print(tokenized_sentence)
  for i in range(1,len(tokenized_sentence)):
    input_sequ.append(tokenized_sentence[:i+1])

input_sequ

#input is of different size so make it same size by padding
max_len=max([len(x) for x in input_sequ])
max_len

from tensorflow.keras.preprocessing.sequence import pad_sequences
paded_input_sequences=pad_sequences(input_sequ,maxlen=max_len,padding='pre')
paded_input_sequences

