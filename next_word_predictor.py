print(y.shape)

#it is a multi class classification so go for one-hot encoding
from tensorflow.keras.utils import to_categorical
y=to_categorical(y,num_classes=len(tokenizer.word_index)+1)

y.shape

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Embedding,LSTM,Dense
max_len = max([len(x) for x in input_sequ])
model=Sequential()
vocab_size = len(tokenizer.word_index) + 1
model.add(Embedding(vocab_size, 100, input_length=max_len-1))
# model.add(Embedding(283,100,input_length=max_len-1))
model.add(LSTM(150))
